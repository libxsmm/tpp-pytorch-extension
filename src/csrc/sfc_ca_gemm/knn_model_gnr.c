/*
 * Compute Euclidean distance between two 3D feature vectors.
 */
static inline double euclidean_distance(const double a[3], const double b[3]) {
    double dx = a[0] - b[0];
    double dy = a[1] - b[1];
    double dz = a[2] - b[2];
    return sqrt(dx*dx + dy*dy + dz*dz);
}

/*
 * Standardize input features using fitted scaler parameters.
 */
static inline void standardize_features(double M, double N, double K, double result[3]) {
    result[0] = (M - SCALER_MEAN[0]) / SCALER_SCALE[0];
    result[1] = (N - SCALER_MEAN[1]) / SCALER_SCALE[1];
    result[2] = (K - SCALER_MEAN[2]) / SCALER_SCALE[2];
}

/*
 * Predict configuration using k-NN algorithm.
 * 
 * For k=1 (nearest neighbor), this finds the training sample with
 * minimum Euclidean distance to the query point in standardized space.
 * Note: TRAINING_X is already standardized, so we only standardize the query.
 */
void predict_config_knn_gnr(int M, int N, int K, int* out_kbf, int* out_K_layers) {
    double query[3];
    standardize_features((double)M, (double)N, (double)K, query);
    
    /* Find k nearest neighbors */
    double min_dist = DBL_MAX;
    int nearest_idx = 0;
    
    for (int i = 0; i < N_SAMPLES; i++) {
        /* Training samples are already standardized, use directly */
        double dist = euclidean_distance(query, TRAINING_X[i]);
        
        /* Update nearest neighbor */
        if (dist < min_dist) {
            min_dist = dist;
            nearest_idx = i;
        }
    }
    
    /* Return predicted configuration from nearest neighbor */
    *out_kbf = TRAINING_Y_KBF[nearest_idx];
    *out_K_layers = TRAINING_Y_K_LAYERS[nearest_idx];
}

/* Get model information */
int get_knn_k_value_gnr(void) {
    return K_VALUE;
}

int get_knn_training_samples_gnr(void) {
    return N_SAMPLES;
}

